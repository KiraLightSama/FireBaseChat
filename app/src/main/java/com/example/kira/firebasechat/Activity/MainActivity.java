package com.example.kira.firebasechat.Activity;

import android.app.Activity;
import android.content.Intent;
import android.content.pm.PackageManager;
import android.net.Uri;
import android.os.Bundle;
import android.support.design.widget.FloatingActionButton;
import android.support.v4.app.ActivityCompat;
import android.support.v7.app.AppCompatActivity;
import android.support.v7.widget.LinearLayoutManager;
import android.support.v7.widget.RecyclerView;
import android.view.View;
import android.widget.EditText;
import android.widget.ImageButton;
import android.widget.ImageView;
import android.widget.TextView;

import com.bumptech.glide.Glide;
import com.example.kira.firebasechat.AdapterMensajes;
import com.example.kira.firebasechat.Entidades.MensajeEnviar;
import com.example.kira.firebasechat.Entidades.MensajeRecibir;
import com.example.kira.firebasechat.Entidades.Usuario;
import com.example.kira.firebasechat.Manifest;
import com.example.kira.firebasechat.R;
